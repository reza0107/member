    public function testCekmemberReturnsFirstMatchForOrCondition(): void
    {
        // Test that cekmember works with either unique_code OR rfid_code
        $this->model->createmember('1234567890123456', 'John Teacher', JenisKelamin::LAKI_LAKI->value, 'Jl. Test', '08123456789', 'RFID123');
        
        $member = $this->db->table('tb_member')->where('', '1234567890123456')->get()->getRowArray();
        
        $resultByUniqueCode = $this->model->cekmember($member['unique_code']);
        $resultByRfid = $this->model->cekmember('RFID123');
        
        $this->assertNotNull($resultByUniqueCode);
        $this->assertNotNull($resultByRfid);
        $this->assertEquals($resultByUniqueCode['id_member'], $resultByRfid['id_member']);
    }

    public function testGenerateCSVObjectSuccessfully(): void
    {
        $csvContent = ",nama_member,jenis_kelamin,alamat,no_hp\n" .
                      "1234567890123456,member One,L,Jl. Test 1,08111111111\n" .
                      "9876543210987654,member Two,P,Jl. Test 2,08222222222\n";

        $tmpDir = FCPATH . 'uploads/tmp/';
        if (!is_dir($tmpDir)) {
            mkdir($tmpDir, 0777, true);
        }

        $tempFile = $tmpDir . 'test_member_' . uniqid() . '.csv';
        file_put_contents($tempFile, $csvContent);

        $result = $this->model->generateCSVObject($tempFile);

        $this->assertNotEmpty($result);
        $this->assertEquals(2, $result->numberOfItems);
        $this->assertNotEmpty($result->txtFileName);

        $txtFile = $tmpDir . $result->txtFileName;
        $this->assertFileExists($txtFile);

        @unlink($txtFile);
    }

