    public function testImportCSVItemSuccessfully(): void
    {
        $testData = [
            [
                '' => '1234567890123456',
                'nama_member' => 'Imported member',
                'jenis_kelamin' => 'L',
                'alamat' => 'Jl. Import Test',
                'no_hp' => '08123456789'
            ]
        ];

        $tmpDir = FCPATH . 'uploads/tmp/';
        if (!is_dir($tmpDir)) {
            mkdir($tmpDir, 0777, true);
        }

        $txtFileName = 'test_' . uniqid() . '.txt';
        $txtFile = fopen($tmpDir . $txtFileName, 'w');
        fwrite($txtFile, serialize($testData));
        fclose($txtFile);

        $result = $this->model->importCSVItem($txtFileName, 1);

        $this->assertNotEmpty($result);
        $this->assertEquals('1234567890123456', $result['']);
        $this->assertEquals('Imported member', $result['nama_member']);
        $this->assertNotEmpty($result['unique_code']);

        $member = $this->db->table('tb_member')
            ->where('', '1234567890123456')
            ->get()
            ->getRowArray();

        $this->assertNotNull($member);
        $this->assertEquals('Imported member', $member['nama_member']);

        @unlink($tmpDir . $txtFileName);
    }

